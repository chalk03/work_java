package pratice;

public class pra02 {

	public static void main(String[] args) {
		Integer i = 50;
		int num1 = i;
		System.out.printf("%d %n", num1);
		
		Byte b = 30;
		byte num2 = b;
		System.out.printf("%d %n", num2);
		
		Long l = 1000000L;
		long num3 = l;
		System.out.printf("%d %n", num3);
		
		Short s = 5;
		short num4 = s;
		System.out.printf("%d %n", num4);
		
		Float f = 2.5F;
		float num5 = f;
		System.out.printf("%f %n", num5);
		
		Double d = 3.5;
		double num6 = d;
		System.out.printf("%f %n", num6);
		
		Character c = 'C';
		char num7 = c;
		System.out.printf("%c %n", num7);
		
		Boolean b1 = true;
		boolean num8 = b1;
		System.out.printf("%b %n", num8);
		
	}
}
